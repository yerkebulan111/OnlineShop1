def categories_keyboard(categories):
    buttons = []
    for category in categories:
        buttons.append([InlineKeyboardButton(
            text=category['name'],
            callback_data=f"category_{category['id']}"
        )])
    return InlineKeyboardMarkup(inline_keyboard=buttons)

